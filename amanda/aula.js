// Var pode ser reatribuído e redeclarar
var rotulo = 'amanda'
console.log(rotulo)

var rotulo = 12345
console.log(rotulo)

// Let não permite redeclarar, mas pode reatribuir o valor
let produto = 'pincel' 
// let produto = 'pincel' 
produto = 'tinta'
console.log(produto)

// Const não permite reatribuir, nem redeclarar, o valor sempre vai ser o mesmo
const numero = 10 
// const numero = 20
// numero = 30
console.log(numero) 